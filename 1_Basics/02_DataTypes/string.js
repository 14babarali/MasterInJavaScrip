// String data type
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName;

console.log(fullName); // Output: John Doe

// String methods
console.log(fullName.toUpperCase()); // Output: JOHN DOE
console.log(fullName.toLowerCase()); // Output: john doe
console.log(fullName.length); // Output: 8
