// Number data type
let num1 = 10;
let num2 = 3.5;

// Arithmetic operations
console.log(num1 + num2); // Output: 13.5
console.log(num1 - num2); // Output: 6.5
console.log(num1 * num2); // Output: 35
console.log(num1 / num2); // Output: 2.857142857142857

// Number methods
console.log(num1.toString()); // Output: "10"
console.log(num2.toFixed(1)); // Output: "3.5"
