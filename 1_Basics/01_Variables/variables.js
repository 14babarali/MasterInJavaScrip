// Add the following content to `variables.js`:
// var example
var x = 10;
console.log(x); // Output: 10

// var can be re-declared and updated
var x = 20;
console.log(x); // Output: 20

// let example
let y = 30;
console.log(y); // Output: 30

// let can be updated but not re-declared in the same scope
y = 40;
console.log(y); // Output: 40

// const example
const z = 50;
console.log(z); // Output: 50

// const cannot be updated or re-declared
// Uncommenting the following lines will throw an error
// z = 60;
// const z = 70;

console.log('var, let, and const examples are successfully executed');
